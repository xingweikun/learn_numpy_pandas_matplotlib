import numpy as np

a = np.arange(10)
A = np.arange(20).reshape(4, 5)
print(a[3], a[5], a[-1])
print(a[2:4])
print(f'A:\n{A}')
# A[0,0]     # 取数组A的0行0列，返回值0
# A[-1,2]    # 取最后一行的第2列，返回值17
# A[2]        # 取第2行所有的列，返回array([10, 11, 12, 13, 14])
# A[-1]       # 取最后1行
# A[0:-1]      # 取除了最后1行之外其它所有行
# A[0:2,2:4]  #取0和1行，2和3列
# A[:,2]         # 取所有行中的第2列

a[4:6] = 520
print(f'a:{a}')
A[:1, :2] = 520
print(f'A:\n{A}')
